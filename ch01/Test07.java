package ch01;
//인용부호 사용 "Text" 또는 'Text'
public class Test07 {
	public static void main(String args[]){
		System.out.println("비둘기");
		System.out.println("닭둘기");
		System.out.println("\"빵둘기\"");
		System.out.println("\'전차둘기\'");
		System.out.println("\\카미카제둘기\\");
		
	}//main_end
}//class_end

/*
 	자바에서는 문자열을 표현할 때 인용부호(따옴표)를 사용합니다. 
 	인용부호는 문자열을 감싸고, 해당 문자열을 문자열 리터럴로 나타냅니다. 
 	다음은 자바에서 인용부호를 사용하는 예시와 설명입니다:

	큰 따옴표 (") 사용:
	문자열을 큰 따옴표로 둘러싸면 해당 문자열은 큰 따옴표 안에 포함됩니다.
	큰 따옴표 안에 어떤 문자도 사용할 수 있으며, 이 문자열은 문자열 리터럴로 간주됩니다.


	String greeting = "안녕하세요, 자바!"; // 큰 따옴표로 둘러싼 문자열 리터럴


	작은 따옴표 (') 사용 (문자 리터럴):
	작은 따옴표로 둘러싸인 문자는 문자 리터럴을 나타냅니다.
	문자 리터럴은 단일 문자를 나타냅니다. 
	예를 들어, 'A', '5', '%'와 같이 작은 따옴표 안에 단일 문자를 넣을 수 있습니다.


	char grade = 'A'; // 작은 따옴표로 둘러싼 문자 리터럴


	이스케이프 문자 사용:
	때로는 특수 문자를 문자열에 포함시키기 위해 이스케이프 문자를 사용합니다. 
	이스케이프 문자는 백슬래시(\)로 시작하며, 특수 문자를 나타내는 데 사용됩니다.
	
	예를 들어, 큰 따옴표를 문자열 안에 포함시키기 위해 \"를 사용할 수 있습니다.


	String message = "이 문자열에는 큰 따옴표(\"), 작은 따옴표(')가 포함됩니다.";
	인용부호를 올바르게 사용하여 문자열을 생성하고, 
	문자나 특수 문자를 포함시키는 데 주의하면 문자열을 올바르게 표현할 수 있습니다.
*/