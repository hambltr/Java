package ch03_operation;

public class Test06 {
	public static void main(String[] args) {
		
		int a=10; // 변수선언, 10으로 초기화
		System.out.println("기본값: "+a+" << 변수 (연산: 2)");
		
		a+=2;
		System.out.println("+= "+a);
		
		a-=2;
		System.out.println("-= "+a);
		
		a*=2;
		System.out.println("*= "+a);
		
		a/=2;
		System.out.println("/= "+a);
		
		a%=2;
		System.out.println("%= "+a);
		
		
	}//main-end
}//class-end
