package ch04_control;
//while , do-while 비교 실습
public class Test19 {
	public static void main(String[] args) {
		//while
		int i=10000;//초기값
		while(i<=10){
			System.out.println("Hi");
			i++;
		}//while
		
		
		//do~while
		int j=10000;//초기값
		do{
			System.out.println("Bye");
			j++;
		} while(j<=10);
		
		
		
	}//main
}//class
