package ch04_control;

public class Test10for {
	public static void main(String[] args) {

		
		for (int i=1; i<=10; i++){
			System.out.print(i+" ");//int + char : 자동형변환
		}//for-end
		
		System.out.println();
		System.out.println("---------------------");
		
		for (int i=10; i>=1; i--){
			System.out.print(i+" ");
		}//for-end
		



	}//main-end
}//class-end
